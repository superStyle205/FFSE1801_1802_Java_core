package Lap5Bai1;

public class SinhVien {
	public String hoTen;
	public double diem;

	public SinhVien(String hoTen, double diem) {
		super();
		this.hoTen = hoTen;
		this.diem = diem;
	}
	public SinhVien() {
		super();
	}
	public String getHoTen() {
		return hoTen;
	}
	public void setHoTen(String hoTen) {
		this.hoTen = hoTen;
	}
	public double getDiem() {
		return diem;
	}
	public void setDiem(double diem) {
		this.diem = diem;
	}



}
